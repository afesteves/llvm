#ifndef LLVM_LIB_TARGET_P3_P3SUBTARGET_H
#define LLVM_LIB_TARGET_P3_P3SUBTARGET_H

#include "P3FrameLowering.h"
#include "P3ISelLowering.h"
#include "P3InstrInfo.h"
#include "P3RegisterInfo.h"
#include "llvm/CodeGen/SelectionDAGTargetInfo.h"
#include "llvm/IR/DataLayout.h"
#include "llvm/Target/TargetSubtargetInfo.h"
#include <string>

#define GET_SUBTARGETINFO_HEADER
#include "P3GenSubtargetInfo.inc"

namespace llvm {
class StringRef;

class P3Subtarget : public P3GenSubtargetInfo {
  virtual void anchor();
  bool ExtendedInsts;
  P3FrameLowering FrameLowering;
  P3InstrInfo InstrInfo;
  P3TargetLowering TLInfo;
  SelectionDAGTargetInfo TSInfo;

public:
  /// This constructor initializes the data members to match that
  /// of the specified triple.
  ///
  P3Subtarget(const Triple &TT, const std::string &CPU,
                  const std::string &FS, const TargetMachine &TM);

  P3Subtarget &initializeSubtargetDependencies(StringRef CPU, StringRef FS);

  /// ParseSubtargetFeatures - Parses features string setting specified
  /// subtarget options.  Definition of function is auto generated by tblgen.
  void ParseSubtargetFeatures(StringRef CPU, StringRef FS);

  const TargetFrameLowering *getFrameLowering() const override {
    return &FrameLowering;
  }
  const P3InstrInfo *getInstrInfo() const override { return &InstrInfo; }
  const TargetRegisterInfo *getRegisterInfo() const override {
    return &InstrInfo.getRegisterInfo();
  }
  const P3TargetLowering *getTargetLowering() const override {
    return &TLInfo;
  }
  const SelectionDAGTargetInfo *getSelectionDAGInfo() const override {
    return &TSInfo;
  }
};
} // End llvm namespace

#endif  // LLVM_TARGET_P3_SUBTARGET_H
